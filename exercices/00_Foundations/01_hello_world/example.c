#include <stdio.h>

int main() {
    // printf() affiche du texte dans le terminal
    printf("Hello, World!\n");

    // Afficher plusieurs lignes
    printf("Bienvenue dans l'apprentissage du C!\n");
    printf("Ceci est ton premier programme.\n");

    // Afficher sans retour à la ligne
    printf("Sans retour ");
    printf("à la ligne.\n");

    // Afficher des caractères spéciaux
    printf("\nCaractères spéciaux :\n");
    printf("Tabulation:\tTexte après tab\n");
    printf("Guillemets: \"Hello\"\n");
    printf("Backslash: C:\\Users\\Documents\n");

    return 0;  // 0 signifie succès
}
